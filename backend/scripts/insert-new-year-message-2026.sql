Dear friends and beloved family in Christ,

As we step into New Year 2026, we are reminded that life is not a perfect straight line. It is a journey with mistakes and new starts, tears and joy, failures and God''s amazing grace.

Some of us may look back at our beginnings, poor background, broken families, wrong decisions, sins, regrets, or missed opportunities, and feel discouraged. But the Bible reminds us that our beginning does not decide our destiny. What truly matters is how we finish and who we become in Christ.

"Better is the end of a thing than the beginning thereof" (Ecclesiastes 7:8).

You and I cannot go back and edit our past. But God can beautifully rewrite our ending. The past may be written, but the future is still open in His hands.

You cannot change the beginning – but you can, with God, change the ending.

Jesus said: "The one who endures to the end will be saved" (Matthew 24:13).

Paul declared near the end of his life: "I have fought the good fight, I have finished the race, I have kept the faith" (2 Timothy 4:7). Paul did not have a perfect beginning, but because he surrendered to Christ, he had a glorious ending.

Your beginning may be painful. Your middle may be messy. But in Christ, your ending can be beautiful.

Not Just Born — But Born Again

Every one of us is born physically, but Jesus said that is not enough: "Unless one is born again, he cannot see the kingdom of God" (John 3:3).

To be born again means:
1. turning from sin
2. believing in Jesus
3. receiving Him as Lord and Saviour
4. allowing the Holy Spirit to transform your heart

"But as many as received him, to them gave he power to become the sons of God, even to them that believe on his name" (John 1: 12).

This is not mere religion or ritual; this is new life from God.

A New Ending: Eternal Life

As we enter 2026, one truth remains: earthly life will one day end. What matters is where we will spend eternity.

"For the wages of sin is death, but the gift of God is eternal life in Christ Jesus our Lord"(Romans 6:23).

Eternal life is God''s gift — not our achievement.

"Whoever believes in Him shall not perish but have eternal life" (John 3:16).

Eternal life means: living forever with God. freedom from sin and death, joy in His presence forever. It begins now when we know Christ.

Start Now — Change the Ending

As you enter 2026, you may feel:
1. My past is too bad.
2. I failed too many times.
3. I am too old to change.
4. I am far from God.

But God gently says: Start now. You cannot rewrite your past years, but today you can:
1. repent
2. believe
3. surrender
4. begin again in Christ

God specializes in changing endings. He turned:
Jacob the deceiver to Israel the prince,
Moses the murderer to Deliverer,
Peter the denier to Apostle and Paul the persecutor to Preacher.

Your story is not over yet. "Be faithful unto death, and I will give you the crown of life" (Revelation 2:10).

My New Year Prayer and Blessing for You

As we enter New Year 2026, my prayer for you is: May God give you a new beginning in your heart. May your faith grow deeper. May your walk with Jesus become closer. May your life finish well in Christ.

Remember: The beginning is not the most important, the ending is Being born once is not enough — being born again is essential. The true goal of life is not success — but eternal life in Christ.

So, this year: Start where you are. Give your life to Jesus. Walk with Him daily. Finish your race in faith.

You cannot change your beginning. But in Christ, you can gloriously change your ending.

