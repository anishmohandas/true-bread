In Paradise Lost, Milton describes the pride of Satan, his fall from heaven, and how he tempted Adam and Eve in the Garden of Eden. The poem shows that disobedience against God brings pain and brokenness into the world. But the Bible tells us even more: not only paradise lost, but also paradise restored through Jesus Christ and His saving work.

Lucifer''s Pride and Fall

Isaiah 14:12-15. How art thou fallen from heaven, O Lucifer, son of the morning! how art thou cut down to the ground, which didst weaken the nations. 13. For thou hast said in thine heart, I will ascend into heaven, I will exalt my throne above the stars of God: I will sit also upon the mount of the congregation, in the sides of the north: 14. I will ascend above the heights of the clouds; I will be like the most High.15. Yet thou shalt be brought down to hell, to the sides of the pit.

In Paradise Lost, Milton pictures this moment of rebellion. Lucifer refuses to submit to God and declares:

"Better to reign in Hell than serve in Heaven."

Milton is not replacing Scripture; he is illustrating the pride Isaiah describes. Pride says: I don''t need God, I will rule my own life, I will be like the Most High.

The Bible shows the result clearly: Pride leads to a fall.

Who is Lucifer? Lucifer was originally created by God as a good angel. But pride grew in his heart. He wanted God''s place instead of God''s presence.

Ezekiel 28: 17. Thine heart was lifted up because of thy beauty, thou hast corrupted thy wisdom by reason of thy brightness: I will cast thee to the ground, I will lay thee before kings, that they may behold thee.

Because of pride and rebellion, he became:

1. Satan = adversary (enemy)
2. Devil = deceiver
3. tempter and accuser

His goal is to pull people away from God: John 10: 10. The thief cometh not, but for to steal, and to kill, and to destroy: I am come that they might have life, and that they might have it more abundantly.

Why did God create humans?

God created us in His image. Genesis 1:26–27 "We are created in the image and likeness of God." This means: we can love, we can think, we can choose, we are made for relationship with God. God did not make robots. He gave free will, real choice.

What happened in the Eden Garden?

Satan came as the serpent in Eden.( Genesis 3:1–6) He used the same tricks he still uses today: he questioned God''s Word, he lied about consequences, he appealed to pride. Humans disobeyed God. This is called sin. (Romans 3: 23) For all have sinned, and come short of the glory of God. Because of sin we see today: guilt, broken families, addictions, hatred and selfishness, death.

In Paradise Lost, Satan hates humanity because God loves humanity. He attacks what God loves. He tempts Adam and Eve by telling them that they can live without God, that their minds and wills are enough. In the poem, he echoes the message of pride:

But Scripture shows the truth: separation from God never leads to freedom, only bondage. But the story does not end there: Life is not just school, friends, phone, or future plans. The Bible says we are part of a big spiritual story. In this story there is:

1. God – our Creator
2. Humans – created in God''s image
3. Lucifer (Satan) – the enemy who rebelled against God

This explains why life is hard, why temptation is real, and why hope is stronger than darkness.

Is Satan ruling the world now?

The Bible says Satan has influence in this world.1 Peter 5: 8. Be sober, be vigilant; because your adversary the devil, as a roaring lion, walketh about, seeking whom he may devour:

But remember this: his power is limited; his time is temporary. God is still in control: Psalms 24: 1. The earth is the LORD''s, and the fulness thereof; the world, and they that dwell therein.

The turning point – Jesus Christ

Jesus came to destroy Satan''s works. 1 John 3:8. He that committeth sin is of the devil; for the devil sinneth from the beginning. For this purpose, the Son of God was manifested, that he might destroy the works of the devil.

On the cross: Romans 5: 8. But God commendeth his love toward us, in that, while we were yet sinners, Christ died for us.

Colossians 2: 15. And having spoiled principalities and powers, he made a shew of them openly, triumphing over them in it.

The resurrection proved: Jesus wins. Satan loses.

Our life today – real battle, real victory

The Christian life is not a game; it is a spiritual battle. Ephesians 6: 12. For we wrestle not against flesh and blood, but against principalities, against powers, against the rulers of the darkness of this world, against spiritual wickedness in high places.

But God gives us armor: Ephesians 6:10–18 The armor of God: truth, righteousness, gospel of peace, faith, salvation, Word of God and prayer. We don''t fight alone or in fear. James 4: 7 Submit yourselves therefore to God. Resist the devil, and he will flee from you.

The Final Victory

The Bible clearly says Satan''s end is certain. Revelation 20: 10. And the devil that deceived them was cast into the lake of fire and brimstone, where the beast and the false prophet are, and shall be tormented day and night for ever and ever.

For believers: Revelation 21: 4. And God shall wipe away all tears from their eyes; and there shall be no more death, neither sorrow, nor crying, neither shall there be any more pain: for the former things are passed away.

Romans 8:37 "In all these things we are more than conquerors through Christ."

Paradise Lost: a simple guide for young people:

1. Lucifer fell through pride
2. Humans fell through disobedience
3. We live in a spiritual battlefield
4. Jesus won the decisive victory on the cross
5. Satan''s power is limited and temporary
6. Final victory belongs to Jesus and His people

Though Satan''s pride led to his fall and Adam and Eve''s disobedience brought sin into the world, the story does not end with failure. God''s love is greater than our mistakes. Through Jesus Christ, paradise that was lost can be restored. As young people, we are invited to choose humility instead of pride, obedience instead of rebellion, and faith instead of doubt. Our past may be broken, but with God, our future can be renewed. Let us walk with Him, trusting that His grace leads us to victory.

When you feel tempted, confused, weak, or afraid, remember: You are not fighting for victory, You are fighting from victory, because Jesus already won.

